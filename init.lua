-- [[ Configure LSP ]]
--  This function gets run when an LSP connects to a particular buffer.
local on_attach = function(_, bufnr)
	nmap('<leader>rn', vim.lsp.buf.rename, bufnr, '[R]e[n]ame')
	nmap('<leader>ca', vim.lsp.buf.code_action, bufnr, '[C]ode [A]ction')

	nmap('gd', vim.lsp.buf.definition, bufnr, '[G]oto [D]efinition')
	nmap('gr', require('telescope.builtin').lsp_references, bufnr, '[G]oto [R]eferences')
	nmap('gI', vim.lsp.buf.implementation, bufnr, '[G]oto [I]mplementation')
	nmap('<leader>D', vim.lsp.buf.type_definition, bufnr, 'Type [D]efinition')
	nmap('<leader>ds', require('telescope.builtin').lsp_document_symbols, bufnr, '[D]ocument [S]ymbols')
	nmap('<leader>ws', require('telescope.builtin').lsp_dynamic_workspace_symbols, bufnr, '[W]orkspace [S]ymbols')

	-- See `:help K` for why this keymap
	nmap('K', vim.lsp.buf.hover, bufnr, 'Hover Documentation')
	nmap('<C-k>', vim.lsp.buf.signature_help, bufnr, 'Signature Documentation')

	-- Lesser used LSP functionality
	nmap('gD', vim.lsp.buf.declaration, bufnr, '[G]oto [D]eclaration')
	nmap('<leader>wa', vim.lsp.buf.add_workspace_folder, bufnr, '[W]orkspace [A]dd Folder')
	nmap('<leader>wr', vim.lsp.buf.remove_workspace_folder, bufnr, '[W]orkspace [R]emove Folder')
	nmap('<leader>wl', function()
		print(vim.inspect(vim.lsp.buf.list_workspace_folders()))
	end, bufnr, '[W]orkspace [L]ist Folders')

	-- Create a command `:Format` local to the LSP buffer
	vim.api.nvim_buf_create_user_command(bufnr, 'Format', function(_)
		vim.lsp.buf.format()
	end, { desc = 'Format current buffer with LSP' })
end

